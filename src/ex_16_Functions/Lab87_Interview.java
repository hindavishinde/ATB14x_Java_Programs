package ex_16_Functions;

public class Lab87_Interview {
    public static void main(String[] args) { // Only  1 main method considered by JVM
    }

    public static void main(String args) { // void function no return type
    }

    public static int main(int args) {
        return 10;
    }

    public static float main(float args) {
        return 3.14f;
    }

    public static boolean main(boolean args) {
        return true;
    }
}

package ex_13_ForLoop;

public class Lab63_For_If_Loop {
    public static void main(String[] args) {
        for (int Hindavi = 1; Hindavi < 25; Hindavi++) {
            if (Hindavi > 20) {
                System.out.println("Phone"); // for 21, 22, 23, 24 runs the o/p will be Phone
            } else {
                System.out.println("No Phone"); // for 1 to 20 runs the o/p will be No Phone
            }

        }
    }
}
